    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
// delete a record
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        // get the selected row
        int selectedRowIndex = jTable1.getSelectedRow();
        String reason = model.getValueAt(selectedRowIndex, 0).toString();
        //System.out.printf(reason);
        //delete record from database
        VehiclesOfInterestController s = new VehiclesOfInterestController();
        s.deleteReasonForInterestBreed(reason);
        //
        model.removeRow(selectedRowIndex);
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        // get the selected row
        int selectedRowIndex = jTable1.getSelectedRow();
        String reason = model.getValueAt(selectedRowIndex, 0).toString();
        String interest = model.getValueAt(selectedRowIndex, 1).toString();
        //update database
        VehiclesOfInterestController s = new VehiclesOfInterestController();
        s.updateReasonForInterest(reason, interest);
    }//GEN-LAST:event_jButtonUpdateActionPerformed

