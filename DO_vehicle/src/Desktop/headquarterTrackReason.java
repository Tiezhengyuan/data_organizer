    public void addTable()
    {
        //retrieve reason of interest
        String[][] reasonInterest;
        VehiclesOfInterestController s = new VehiclesOfInterestController();
        reasonInterest = s.getAllReasonsForInterest();

        //
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object rowData [] = new Object[2];
        for (int i=0; i<reasonInterest.length; i++){
           //rowData[0] = i+1; 
           rowData[0] = reasonInterest[i][0]; 
           rowData[1] = reasonInterest[i][1]; 
           model.addRow(rowData);
        }
        
    }
        
