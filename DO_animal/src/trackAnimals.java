    private void jButtonShowAnimalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonShowAnimalActionPerformed
        animalSetName = jComboBoxAnimalSet.getSelectedItem().toString();
        //show animal set name in text field
        jTextAnimalSet.setText(animalSetName);
        //show animal data in text area
        try {
            //get animal data
            s.fromFile("animalSets\\"+animalSetName);
            //
            String block = s.getAnimalBlock();
            //
            jTextAreaAnimals.setText(block);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(trackAnimals.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InvalidAnimalException ex) {
            Logger.getLogger(trackAnimals.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonShowAnimalActionPerformed

    private void jButtonExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExportActionPerformed
        // TODO add your handling code here:
        String outDir = jTextOutDir.getText();
        File d = new File(outDir);
        if (d.isDirectory()){
            try {
                s.toTrackFile(outDir+"\\"+animalSetName+".csv");
            } catch (FileNotFoundException ex) {
                Logger.getLogger(trackAnimals.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jButtonExportActionPerformed

